@pytest.mark.parametrize("shell,set_command,unset_command",
                         [("--bat", 'set "%s=%s"', 'set "%s="')]
                         if sys.platform == "win32" else [("--sh", "export %s=%s", "unset %s"),
                                                          ("--csh", "setenv %s %s", "unsetenv %s")])
def test_unload(shell, set_command, unset_command, install_mockery, mock_fetch, mock_archive, mock_packages, working_env):
    """Tests that any variables set in the user environment are undone by the
    unload command"""
    install("mpileaks")
    mpileaks_spec = spack.spec.Spec("mpileaks").concretized()

    # Set so unload has something to do
    os.environ["FOOBAR"] = "mpileaks"
    os.environ[uenv.spack_loaded_hashes_var] = "%s%c%s" % (
        mpileaks_spec.dag_hash(),
        os.pathsep,
        "garbage",
    )
    out = unload(shell, "mpileaks")
    assert unset_command % "FOOBAR" in out

    assert set_command % (uenv.spack_loaded_hashes_var, "garbage") in out


