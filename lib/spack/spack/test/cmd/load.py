pytestmark = pytest.mark.not_on_windows("does not run on windows")

@pytest.mark.parametrize("shell,command_end", [("--bat", "")]
                         if sys.platform == "win32" else [("--sh", ";")])
def test_manpath_trailing_colon(
    shell, command_end, install_mockery, mock_fetch, mock_archive, mock_packages, working_env
):
    """Test that the commands generated by load add the MANPATH prefix
    inspections. Also test that Spack correctly preserves the default/existing
    manpath search path via a trailing colon"""
    install("mpileaks")

    bat_out = load(shell, "--only", "package", "mpileaks")
    lines = bat_out.split("\n")
    assert any(re.match(r'set "MANPATH=.*' + os.pathsep + command_end + '"', ln) for ln in lines)

    os.environ["MANPATH"] = "/tmp/man" + os.pathsep

    bat_out = load(shell, "--only", "package", "mpileaks")
    lines = bat_out.split("\n")
    assert any(re.match(r'set "MANPATH=.*' + os.pathsep + '/tmp/man' + os.pathsep + command_end, ln) for ln in lines)


@pytest.mark.parametrize("shell,set_command", [("--bat", 'set "%s=%s"')]
                         if sys.platform == "win32" else [("--sh", "export %s=%s"),
                                                          ("--csh", "setenv %s %s")])
def test_load(shell, set_command, install_mockery, mock_fetch, mock_archive, mock_packages):
    """Test that the commands generated by load add the specified prefix
    inspections. Also test that Spack records loaded specs by hash in the
    user environment.

    CMAKE_PREFIX_PATH is the only prefix inspection guaranteed for fake
    packages, since it keys on the prefix instead of a subdir."""
    install_out = install("mpileaks", output=str, fail_on_error=False)
    print("spack install mpileaks")
    print(install_out)
    mpileaks_spec = spack.spec.Spec("mpileaks").concretized()

    out = load(shell, "--only", "package", "mpileaks")

    # Test prefix inspections
    out_test = set_command % ("CMAKE_PREFIX_PATH", mpileaks_spec.prefix)
    assert out_test in out

    # Test hashes recorded properly
    hash_test_replacements = (uenv.spack_loaded_hashes_var, mpileaks_spec.dag_hash())
    hash_test = set_command % hash_test_replacements
    assert hash_test in out


@pytest.mark.parametrize("shell,set_command", [("--bat", 'set "%s=%s"')]
                         if sys.platform == "win32" else [("--sh", "export %s=%s"),
                                                          ("--csh", "setenv %s %s")])
def test_load_recursive(shell, set_command, install_mockery, mock_fetch, mock_archive, mock_packages):
    """Test that the '-r' option to the load command prepends dependency prefix
    inspections in post-order"""
    install("mpileaks")
    mpileaks_spec = spack.spec.Spec("mpileaks").concretized()

    out = load(shell, "mpileaks")

    # Test prefix inspections
    prefix_test_replacement = os.pathsep.join(
        reversed([s.prefix for s in mpileaks_spec.traverse(order="post")])
    )

    prefix_test = set_command % ("CMAKE_PREFIX_PATH", prefix_test_replacement)
    assert prefix_test in out

    # Test spack records loaded hashes properly
    hash_test_replacement = (
        uenv.spack_loaded_hashes_var,
        os.pathsep.join(reversed([s.dag_hash() for s in mpileaks_spec.traverse(order="post")])),
    )
    sh_hash_test = set_command % hash_test_replacement
    assert sh_hash_test in out


